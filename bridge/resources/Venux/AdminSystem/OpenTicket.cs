namespace Venux.Tickets
{
    public class openTicket
    {
        public string author
        {
            get;
            set;
        }

        public string description
        {
            get;
            set;
        }

        public int id
        {
            get;
            set;
        }

        public openTicket(string author, string description, int id)
        {
            this.author = author;
            this.description = description;
            this.id = id;
        }
    }
}

